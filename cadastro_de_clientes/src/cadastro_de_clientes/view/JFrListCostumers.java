    @Override
    protected String[][] getRows() {
        fetchCostumers();
        String[][] rows = new String[costumers.size()][12];

        for (int i = 0; i < costumers.size(); i++) {
            Costumer c = costumers.get(i);
            rows[i][0] = String.valueOf(c.getId());
            rows[i][1] = c.getName();
            rows[i][2] = c.getPhone();
            rows[i][3] = c.getEmail();
            rows[i][4] = c.getCep();
            rows[i][5] = c.getStreet();
            rows[i][6] = c.getNumber();
            rows[i][7] = c.getComplement();
            rows[i][8] = c.getNeighborhood();
            rows[i][9] = c.getCity();
            rows[i][10] = c.getState();
            rows[i][11] = c.getCpf();
        }
        return rows;
    }

