    private void fetchCostumers() {
        costumers.clear();
        String sql = "SELECT * FROM costumer";

        try (Connection conn = Database.getConnection(); Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery(sql)) {

            while (rs.next()) {
                costumers.add(new Costumer(
                        rs.getInt("id"),
                        rs.getString("name"),
                        rs.getString("phone"),
                        rs.getString("email"),
                        rs.getString("cep"),
                        rs.getString("street"),
                        rs.getString("number"),
                        rs.getString("complement"),
                        rs.getString("neighborhood"),
                        rs.getString("city"),
                        rs.getString("state"),
                        rs.getString("cpf")
                ));
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    @Override
    protected void deleteItem(int selectedRow) {
        String sql = "DELETE FROM costumer WHERE id = ?";
        Costumer c = getSelectedCostumer(selectedRow);
        try (java.sql.Connection conn = Database.getConnection(); java.sql.PreparedStatement stmt = conn.prepareStatement(sql)) {

            stmt.setInt(1, c.getId());
            int rowsAffected = stmt.executeUpdate();

            if (rowsAffected > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Cliente excluído com sucesso!");
                costumers.remove(selectedRow);
                listData();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Falha ao excluir cliente.");
            }

        } catch (java.sql.SQLException ex) {
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
        }
    }

    @Override
    protected void editItem(int selectedRow) {

        Costumer c = getSelectedCostumer(selectedRow);

        JFrRegistryCostumer form = new JFrRegistryCostumer();

        form.getNameText().setText(c.getName());
        form.getPhoneNumberText().setText(c.getPhone());
        form.getEmailText().setText(c.getEmail());
        form.getCpfText().setText(c.getCpf());
        form.getCepText().setText(c.getCep());
        form.getStreetText().setText(c.getStreet());
        form.getNumberText().setText(c.getNumber());
        form.getComplementText().setText(c.getComplement());
        form.getNeighborhoodText().setText(c.getNeighborhood());
        form.getCityText().setText(c.getCity());
        form.getStateComboBox().setSelectedItem(c.getState());

        for (ActionListener al : form.getSaveButton().getActionListeners()) {
            form.getSaveButton().removeActionListener(al);
        }

        form.getSaveButton().addActionListener(evt -> {
            c.setName(form.getNameText().getText());
            c.setPhone(form.getPhoneNumberText().getText());
            c.setEmail(form.getEmailText().getText());
            c.setCpf(form.getCpfText().getText());
            c.setCep(form.getCepText().getText());
            c.setStreet(form.getStreetText().getText());
            c.setNumber(form.getNumberText().getText());
            c.setComplement(form.getComplementText().getText());
            c.setNeighborhood(form.getNeighborhoodText().getText());
            c.setCity(form.getCityText().getText());
            c.setState((String) form.getStateComboBox().getSelectedItem());

            String sql = "UPDATE costumer SET name=?, phone=?, email=?, cpf=?, cep=?, street=?, number=?, complement=?, neighborhood=?, city=?, state=? WHERE id=?";
            try (java.sql.Connection conn = Database.getConnection(); java.sql.PreparedStatement stmt = conn.prepareStatement(sql)) {

                stmt.setString(1, c.getName());
                stmt.setString(2, c.getPhone());
                stmt.setString(3, c.getEmail());
                stmt.setString(4, c.getCpf());
                stmt.setString(5, c.getCep());
                stmt.setString(6, c.getStreet());
                stmt.setString(7, c.getNumber());
                stmt.setString(8, c.getComplement());
                stmt.setString(9, c.getNeighborhood());
                stmt.setString(10, c.getCity());
                stmt.setString(11, c.getState());
                stmt.setInt(12, c.getId());

                stmt.executeUpdate();

                javax.swing.JOptionPane.showMessageDialog(form, "Cliente atualizado com sucesso!");
                listData();
                form.dispose();

            } catch (java.sql.SQLException ex) {
                ex.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(form, "Erro: " + ex.getMessage());
            }
        });

        form.setVisible(true);
    }

    public Costumer getSelectedCostumer(int rowIndex) {
        if (rowIndex >= 0 && rowIndex < costumers.size()) {
            return costumers.get(rowIndex);
        }
        return null;
    }

